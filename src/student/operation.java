    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int sID = Integer.parseInt(ID.getText());
            String query = "SELECT * FROM ADDSTUDENT WHERE ID = '"+sID+"'";
            rs = stmt.executeQuery(query);
            
            if(rs.next()){
                name.setText(rs.getString("stdName"));
                className.setText(rs.getString("stdClass"));
                fatherName.setText(rs.getString("stdFatherName"));
                blood.setText(rs.getString("stdBloodGroup"));
                phoneNumber.setText(rs.getString("stdPhoneNumber"));
                city.setText(rs.getString("stdCity"));
            }else{
                JOptionPane.showMessageDialog(null, "Data Not found");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

