    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try{
            stmt = conn.createStatement();
            int sID = Integer.parseInt(ID.getText());
            String stdName = name.getText();
            String stdClass = className.getText();
            String stdFatherName = fatherName.getText();
            String stdBloodGroup = (String)blood.getText();
            String stdPhoneNumber = phoneNumber.getText();
            String stdCityName = city.getText();
            
            String query = "UPDATE ADDSTUDENT SET stdName='"+stdName+"',stdClass='"+stdClass+"',stdFatherName='"+stdFatherName+"',stdBloodGroup='"+stdBloodGroup+"',stdPhoneNumber='"+stdPhoneNumber+"',stdCity='"+stdCityName+"' WHERE ID='"+sID+"'";
            stmt.executeUpdate(query);
            
            JOptionPane.showMessageDialog(null, "Successfully Updated Data");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
     
    }//GEN-LAST:event_jButton1ActionPerformed

